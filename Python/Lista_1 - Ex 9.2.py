'''
9.2. Peça ao usuário para digitar uma nota (0 a 100) e imprima se ele foi
aprovado (nota >= 60) ou reprovado.
'''

nota = float(input('Digite sua nota: '))

if nota >= 60:
    print('Você foi aprovado! ^-^')
else:
    print('Você foi reprovado, estude mais! :(')

