# Loop For
'''
9.3. Crie uma lista com os nomes de cinco cidades. Use um loop for para imprimir
cada nome de cidade.
'''

cities = ['São paulo','Rio de Janeiro', 'Brasilia', 'Salvador', 'Fortaleza']

print(f'\nAs cidades presentes em nosso catálogo de viagens são: ')

for city in cities:
    print(city)

